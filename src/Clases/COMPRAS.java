    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        String b = this.cantidad.getText();
        int c = Integer.parseInt(b);
        String a = this.precio.getText();
        int d = Integer.parseInt(a);
        int x = c * d;
        b = String.valueOf(x);
        this.sub.setText(b);
        int z = 16;
        b = String.valueOf(z);
        this.iva.setText(b);
        int w = (16 * x) / 100;
        x = x + w;
        b = String.valueOf(x);
        this.total.setText(b);

        if (articulo.getText().isEmpty() || cantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa bien los datos", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            articulo.setText("");
            cantidad.setText("");
            articulo.requestFocus();
            cantidad.requestFocus();
        } else {
            try {
                //res = conexiones.conexion.Consulta("Select COUNT(idCompra)from Compras where idCompra='" + ID.getText() + "'");
                try {
                    while (res.next()) {
                        cont = res.getInt(1);
                    }
                } catch (SQLException e) {
                }
                if (cont >= 1) {
                    JOptionPane.showMessageDialog(this, "Esta compra ya existe", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    //procedimiento.EntradaCompras(articulo.getText(), cantidad.getText());
                    articulo.setText("");
                    cantidad.setText("");
                    precio.setText("");
                    articulo.requestFocus();
                    cantidad.requestFocus();
                    precio.requestFocus();
                    JOptionPane.showMessageDialog(this, "Datos almacenados correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException e) {
            }
        }

    }//GEN-LAST:event_agregarActionPerformed

