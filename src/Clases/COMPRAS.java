    public void Cargar() {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
        conn = conexiones.Conexion("gama", "gama");
        try {
            PreparedStatement consulta = conn.prepareStatement("SELECT * FROM Articulo");
            res = consulta.executeQuery();
            while (res.next()) {
                Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getInt(2));
                v.add(res.getString(3));
                v.add(res.getInt(4));
                modelo.addRow(v);
                tabla.setModel(modelo);
            }
            conexiones.cierraConexion();
        } catch (SQLException e) {
            System.out.println("Hubo un error.");
        }
    }

